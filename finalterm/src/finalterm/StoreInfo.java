package finalterm;

public class StoreInfo {
	String title;
	String address;
	String mapx;
	String mapy;
	String link;
	String tele;
	String category;

	StoreInfo(String title, String address, String mapx, String mapy, String link, String tele, String category) {
		this.title = title;
		this.address = address;
		this.mapx = mapx;
		this.mapy = mapy;
		this.link = link;
		this.tele = tele;
		this.category = category;
	}

}
